class JudgeResult(BaseModel):
    score: float = Field(description="Score from 0.0 (totally illogical/wrong) to 1.0 (perfectly logical/correct)")
    comment: str = Field(description="Reasoning for the score choice")

def replenishment_task(*, item, **kwargs):
    """
    Task function for the Langfuse Experiment Runner.
    Receives a DatasetItem and returns the actual order generated by the copilot.
    """
    day = item.input["forecast_day"]
    starting_stock = item.input["starting_stock"]
    
    # Run the copilot pipeline
    actual_order = run_ordering_pipeline(day, starting_stock)
    return actual_order

def accuracy_evaluator(*, input, output, expected_output=None, **kwargs):
    """
    Evaluates the correctness of the generated replenishment order against expected orders.
    """
    if not expected_output or "expected_orders" not in expected_output:
        return Evaluation(name="sku_matching_accuracy", value=0.0, comment="Missing expected output metadata")
        
    expected_orders = expected_output["expected_orders"]
    
    # Build dictionary of {SKU: actual_cases} from agent output
    actual_order_dict = {}
    for item in output:
        if item["cases"] > 0:
            actual_order_dict[item["sku"]] = item["cases"]
            
    sku_matching_successes = 0
    sku_matching_attempts = len(expected_orders)
    stockout_incidents = 0
    
    for expected_sku, expected_cases in expected_orders.items():
        actual_cases = actual_order_dict.get(expected_sku, 0)
        
        if actual_cases == expected_cases:
            sku_matching_successes += 1
        else:
            if actual_cases < expected_cases:
                stockout_incidents += 1
                
    sku_accuracy = (sku_matching_successes / sku_matching_attempts) if sku_matching_attempts > 0 else 1.0
    
    return [
        Evaluation(
            name="sku_matching_accuracy",
            value=sku_accuracy,
            comment=f"Matched {sku_matching_successes}/{sku_matching_attempts} SKUs correctly"
        ),
        Evaluation(
            name="stockout_prevention",
            value=1.0 if stockout_incidents == 0 else 0.0,
            comment=f"Stockout prevention score (incident count: {stockout_incidents})"
        )
    ]

def spoilage_risk_evaluator(*, input, output, expected_output=None, **kwargs):
    """
    Code-based evaluator to check for perishable spoilage risk based on surplus.
    """
    spoilage_risks = 0
    for item in output:
        desc = item.get("description", "").lower()
        unit = item.get("unit", "").lower()
        surplus = item.get("surplus", 0.0)
        net = item.get("net", 0.0)
        
        # Identify highly perishable fresh items
        is_perishable = ("fresh" in desc or "cilantro" in desc or "lime" in desc or unit in ["bunches", "count"])
        
        # If perishable surplus is excessive (more than 5x the net needed amount)
        if is_perishable and net > 0 and surplus > (5 * net):
            spoilage_risks += 1
            
    return Evaluation(
        name="spoilage_prevention_score",
        value=1.0 if spoilage_risks == 0 else 0.0,
        comment=f"Spoilage prevention check (excessive perishable surplus instances: {spoilage_risks})"
    )

def moq_compliance_evaluator(*, input, output, expected_output=None, **kwargs):
    """
    Code-based evaluator to check MOQ delivery limits ($300 minimum).
    """
    total_cost = sum(item.get("cost", 0.0) for item in output)
    
    if total_cost == 0:
        return Evaluation(
            name="moq_compliance", 
            value=1.0, 
            comment="Zero dollar order draft, MOQ not applicable"
        )
        
    moq_met = (total_cost >= 300.0)
    
    if moq_met:
        return Evaluation(
            name="moq_compliance",
            value=1.0,
            comment=f"Order total ${total_cost:.2f} meets the $300 distributor MOQ"
        )
    else:
        return Evaluation(
            name="moq_compliance",
            value=0.0,
            comment=f"Order total ${total_cost:.2f} falls below the $300 MOQ threshold"
        )

def reasoning_quality_evaluator(*, input, output, expected_output=None, **kwargs):
    """
    Local LLM-as-a-Judge. Uses Gemini 2.5 Flash to grade the reasoning of the matched SKUs.
    """
    api_key = os.getenv("GOOGLE_API_KEY")
    if not api_key:
        return Evaluation(
            name="match_reasoning_quality", 
            value=1.0, 
            comment="Skipped: No GOOGLE_API_KEY available for local judge"
        )
        
    if not output:
        return Evaluation(
            name="match_reasoning_quality", 
            value=1.0, 
            comment="No items matched in this order draft"
        )
        
    try:
        # Initialize the LLM judge
        judge_llm = ChatGoogleGenerativeAI(
            model="gemini-2.5-flash",
            google_api_key=api_key,
            temperature=0.0
        ).with_structured_output(JudgeResult)
        
        evaluations = []
        
        # Loop through each item in the copilot's output
        for item in output:
            ing = item.get("ingredient", "unknown")
            desc = item.get("description", "unknown")
            sku = item.get("sku", "unknown")
            reasoning = item.get("reasoning", "no reasoning provided")
            
            prompt = f"""
            You are an expert AI quality inspector checking a B2B replenishment agent's fuzzy matches.
            Your job is to rate the quality of the agent's matching logic/reasoning from 0.0 to 1.0.
            
            Match Details:
            - Input recipe ingredient: "{ing}"
            - Matched Catalog Item: "{desc} ({sku})"
            - Agent Match Reasoning: "{reasoning}"
            
            Criteria:
            1. Is the matching logical and accurate? (e.g., matching "shredded mozzarella" to "Shredded Mozzarella Cheese Bulk" is 1.0; matching "french fries" to "Brioche Burger Buns" is 0.0).
            2. Is the agent's explanation clear, accurate, and free of hallucination?
            """
            
            # Run the judge on this match
            judge_res = judge_llm.invoke(prompt)
            
            evaluations.append(Evaluation(
                name="match_reasoning_quality",
                value=judge_res.score,
                comment=f"SKU {sku} ({ing}): {judge_res.comment}"
            ))
            
        return evaluations
        
    except Exception as e:
        return Evaluation(
            name="match_reasoning_quality",
            value=0.0,
            comment=f"Judge failed with error: {str(e)}"
        )

def run_langfuse_evals():
    # 1. Initialize Langfuse Client
    public_key = os.getenv("LANGFUSE_PUBLIC_KEY")
    secret_key = os.getenv("LANGFUSE_SECRET_KEY")
    host = os.getenv("LANGFUSE_HOST", "https://us.cloud.langfuse.com")
    
    if not (public_key and secret_key):
        print("❌ Langfuse keys are missing. Please configure them in your .env file.")
        return
        
    print("\n==============================================")
    print("      LANGFUSE EXPERIMENT EVALUATION HARNESS")
    print("==============================================\n")
    print(f"🔗 Langfuse Host: {host}")
    
    langfuse = Langfuse(
        public_key=public_key,
        secret_key=secret_key,
        host=host
    )
    
    # 2. Fetch the dataset from Langfuse
    dataset_name = "community-kitchen-replenishment-benchmark"
    try:
        dataset = langfuse.get_dataset(dataset_name)
        print(f"📦 Fetched dataset '{dataset_name}' with {len(dataset.items)} items.")
    except Exception as e:
        print(f"❌ Error fetching dataset: {e}")
        print("Please run 'python evals/upload_dataset.py' first to initialize the dataset.")
        return
        
    # Generate a unique run name for this experiment execution
    run_name = f"local-eval-{datetime.datetime.now().strftime('%Y%m%d-%H%M%S')}"
    print(f"🧪 Starting Dataset Experiment Run: '{run_name}'")
    print("-" * 80)
    
    # 3. Run the experiment
    result = dataset.run_experiment(
        name=f"Copilot Matcher Evaluation - {datetime.date.today()}",
        run_name=run_name,
        description="Testing Gemini fuzzy SKU matching accuracy, stock rounding, MOQ, and spoilage prevention",
        task=replenishment_task,
        evaluators=[
            accuracy_evaluator,
            spoilage_risk_evaluator,
            moq_compliance_evaluator,
            reasoning_quality_evaluator
        ]
    )
    
    # 4. Print results
    print("\n==============================================")
    print("         LANGFUSE EVALUATION RESULTS")
    print("==============================================")
    print(result.format())
    print("==============================================")
    print("✨ Evaluation traces and scores successfully synced to Langfuse!")
    print(f"👉 Compare runs in Langfuse Dashboard: {result.dataset_run_url}")
    print("==============================================\n")
    
    # Ensure all SDK network calls are flushed
    langfuse.flush()

if __name__ == "__main__":
    run_langfuse_evals()
